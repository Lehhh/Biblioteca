     public void readPesquisar(){
        
        DefaultTableModel pesquisa = (DefaultTableModel) PaReservas.getModel();
        pesquisa.setNumRows(0);
        
        ReservaDAO rdao = new ReservaDAO();
        
        for(Reserva r:rdao.readRAID()){
            
            pesquisa.addRow(new Object[]{
            r.getId(),
            r.getId_a(),
            r.getNome(),
            r.getId_l(),
            r.getTituloo(),
            r.getDatarR(),
            r.getDataeE()     
        });
        
        
        
      }
    }
 
           
    
        
   
