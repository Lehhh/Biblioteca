package model.bean;

public class Reserva {
    
    private int id;
    private int id_a;
    public String tituloo;
    public String dataeE;
    public String datarR;
    public String pendente;
    public int id_l;
    public String nome;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    
    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
    

    public int getId_a() {
        return id_a;
    }

    public void setId_a(int id_a) {
        this.id_a = id_a;
    }

    public String getTituloo() {
        return tituloo;
    }

    public void setTituloo(String tituloo) {
        this.tituloo = tituloo;
    }

    public String getDataeE() {
        return dataeE;
    }

    public void setDataeE(String dataeE) {
        this.dataeE = dataeE;
    }

    public String getDatarR() {
        return datarR;
    }

    public void setDatarR(String datarR) {
        this.datarR = datarR;
    }

    public String getPendente() {
        return pendente;
    }

    public void setPendente(String pendente) {
        this.pendente = pendente;
    }

    public int getId_l() {
        return id_l;
    }

    public void setId_l(int id_l) {
        this.id_l = id_l;
    }
    
    
    
}


